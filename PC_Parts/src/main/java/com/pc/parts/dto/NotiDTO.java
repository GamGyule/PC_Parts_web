package com.pc.parts.dto;

public class NotiDTO {
	
	private int idx;
	private String from_co;
	private String to_co;
	private int pid;
	private int cnt;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getFrom_co() {
		return from_co;
	}
	public void setFrom_co(String from_co) {
		this.from_co = from_co;
	}
	public String getTo_co() {
		return to_co;
	}
	public void setTo_co(String to_co) {
		this.to_co = to_co;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}

	
}
